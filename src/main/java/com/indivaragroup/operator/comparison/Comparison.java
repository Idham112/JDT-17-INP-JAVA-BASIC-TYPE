package com.indivaragroup.operator.comparison;

public class Comparison {
    public static void ComparisonOperator(){
        int a = 5;
        int b = 11;

        System.out.println(a > b);  //false
        System.out.println(a < b);  //true
        System.out.println(a == b); //false
        System.out.println(a >= b); //false
        System.out.println(a <= b); //true
        System.out.println(a != b); //true
    }
}
